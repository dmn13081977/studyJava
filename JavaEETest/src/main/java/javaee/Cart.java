package javaee;

public class Cart {
    String itemName;
    int quantity;

    public String getItemName() {

        return itemName;
    }

    public void setItemName(String name) {
        this.itemName = name;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
